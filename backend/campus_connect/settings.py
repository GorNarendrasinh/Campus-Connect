def _parse_bool(value, default=True):
    """Parse potentially invalid boolean env values safely.

    Falls back to `default` when the value does not match common boolean strings.
    """
    if isinstance(value, bool):
        return value
    if value is None:
        return default
    s = str(value).strip().lower()
    truthy = {"1", "true", "t", "yes", "y", "on"}
    falsy = {"0", "false", "f", "no", "n", "off"}
    if s in truthy:
        return True
    if s in falsy:
        return False
    return default

